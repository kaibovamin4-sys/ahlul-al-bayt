function openMarjaModal(key) {
  const d = marjaData[key];
  document.getElementById('marjaModalLabel').textContent = d.label;
  document.getElementById('marjaModalTitle').textContent = d.title;
  document.getElementById('marjaModalArabic').textContent = d.arabic;
  document.getElementById('marjaModalBody').innerHTML = d.body;
  document.getElementById('marjaModal').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeMarjaModal() {
  document.getElementById('marjaModal').classList.remove('open');
  document.body.style.overflow = '';
}
