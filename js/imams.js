function openImamModal(num) {
  const d = imamData[num];
  document.getElementById('imamModalLabel').textContent = d.label;
  document.getElementById('imamModalTitle').textContent = d.title;
  document.getElementById('imamModalArabic').textContent = d.arabic;
  document.getElementById('imamModalBody').innerHTML = d.body;
  document.getElementById('imamModal').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeImamModal() {
  document.getElementById('imamModal').classList.remove('open');
  document.body.style.overflow = '';
}
